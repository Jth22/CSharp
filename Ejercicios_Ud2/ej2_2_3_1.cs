/* (2.2.3.1) Crea un programa que muestre los números del 10 al 20, ambos
 * incluidos, usando "for".
 * Por Jonathan Sirvent Pedreño
 */
 
 using System;
 
 class ej2_2_3_1
 {
     static void Main()
     {
         for (int i = 10 ; i <= 20 ; i++ )
         {
             Console.WriteLine(i);
         }
     }
 }





